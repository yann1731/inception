CREATE USER IF NOT EXISTS root@localhost IDENTIFIED BY $MYSQL_ROOT_PASSWORD;
SET PASSWORD FOR root@localhost = PASSWORD($MYSQL_ROOT_PASSWORD);
GRANT ALL ON *.* TO root@localhost WITH GRANT OPTION;
CREATE USER IF NOT EXISTS root@'%' IDENTIFIED BY $MYSQL_ROOT_PASSWORD;
SET PASSWORD FOR root@'%' = PASSWORD($MYSQL_ROOT_PASSWORD);
GRANT ALL ON *.* TO root@'%' WITH GRANT OPTION;
CREATE USER IF NOT EXISTS $WP_USER@'%' IDENTIFIED BY $WP_USER_PASS;
SET PASSWORD FOR $WP_USER@'%' = PASSWORD($WP_USER_PASS);
CREATE DATABASE IF NOT EXISTS $WORDPRESS_DB_NAME;
GRANT ALL ON $WORDPRESS_DB_NAME.* TO $WP_USER@'%';